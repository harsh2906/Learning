package com.test.pojo;

public class Age {
	
	String ageX;
	String ageY;
	public Age(String ageX, String ageY)
	{
		this.ageX = ageX;
		this.ageY=ageY;
	}


		public String getAgeX() {
			return ageX;
		}

		public void setAgeX(String ageX) {
			this.ageX = ageX;
		}

		public String getAgeY() {
			return ageY;
		}

		public void setAgeY(String ageY) {
			this.ageY = ageY;
		}


}
